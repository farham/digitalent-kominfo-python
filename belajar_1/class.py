class kalkulator:
    
    def tambah(x, y):
        hasil = x + y
        print ("Hasilnya Pertambahan = ", hasil)
    def kurang(x, y):
        hasil = x - y
        print ("Hasilnya Pengurangan = ", hasil)
    def kali(x, y):
        hasil = x * y
        print ("Hasilnya Perkalian = ", hasil)
    def bagi(x, y):
        hasil = x / y
        print ("Hasilnya Pembagian = ", hasil)

kalkulator.tambah(5, 5)
kalkulator.kurang(5, 5)
kalkulator.kali(5, 5)
kalkulator.bagi(5, 5)